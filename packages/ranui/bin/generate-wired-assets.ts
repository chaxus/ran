/**
 * Offline wired SVG border-image asset generator.
 *
 * Generates a small library of hand-drawn-looking SVG rectangle fragments
 * using RoughJS with a fixed seed, then writes them as inline data URIs into
 * theme-packs/wired-assets.less.
 *
 * Run this script whenever stroke parameters change:
 *   pnpm tsx bin/generate-wired-assets.ts
 *
 * The output file is committed to source. It must NOT be generated at
 * browser runtime — importing the wired pack adds no RoughJS dependency to
 * consumer bundles.
 *
 * Usage:
 *   pnpm add -D roughjs          (one-time devDependency install)
 *   pnpm tsx bin/generate-wired-assets.ts
 */

import { writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const OUTPUT = resolve(ROOT, 'theme-packs/wired-assets.less');

const SEED = 42;
const SVG_SIZE = 32;
const SLICE = 8;
const BORDER_WIDTH = 4;

// State configurations: [strokeWidth, strokeColor, roughness, dashArray]
const STATES: Array<{
  name: string;
  strokeWidth: number;
  stroke: string;
  roughness: number;
  dashArray?: string;
}> = [
  { name: 'normal', strokeWidth: 1.5, stroke: '#111827', roughness: 1.2 },
  { name: 'hover', strokeWidth: 2, stroke: '#111827', roughness: 1.5 },
  { name: 'focus', strokeWidth: 2.5, stroke: '#111827', roughness: 1.8 },
  { name: 'active', strokeWidth: 2, stroke: '#111827', roughness: 1.0 },
  { name: 'disabled', strokeWidth: 1.5, stroke: '#9ca3af', roughness: 0.8, dashArray: '4,2' },
];

/**
 * Generates a rough rectangle SVG using RoughJS.
 *
 * When RoughJS is available as a devDependency, replace the placeholder body
 * below with the actual RoughJS call:
 *
 *   import rough from 'roughjs';
 *   const svg = document.createElementNS('http://www.w3.org/2000/svg', 'svg');
 *   svg.setAttribute('width', String(SVG_SIZE));
 *   svg.setAttribute('height', String(SVG_SIZE));
 *   const rc = rough.svg(svg);
 *   const rect = rc.rectangle(SLICE / 2, SLICE / 2, SVG_SIZE - SLICE, SVG_SIZE - SLICE, {
 *     seed: SEED,
 *     roughness: config.roughness,
 *     strokeWidth: config.strokeWidth,
 *     stroke: config.stroke,
 *     fill: 'none',
 *   });
 *   svg.appendChild(rect);
 *   return svg.outerHTML;
 */
function generateSVG(config: (typeof STATES)[0]): string {
  // Placeholder: hand-crafted wavy paths that approximate RoughJS output.
  // Replace this function body with the RoughJS implementation above once
  // roughjs is added as a devDependency.
  const { strokeWidth, stroke, roughness, dashArray } = config;
  const r = roughness;
  const encodedStroke = stroke.replace(/#/g, '%23');
  const dashAttr = dashArray ? ` stroke-dasharray='${dashArray}'` : '';

  // Wavy rectangle path — vertices jitter by ±roughness from each corner,
  // control points pull edges slightly inward/outward.
  const t = (2 + r).toFixed(1); // top Y
  const b = (SVG_SIZE - 2 - r).toFixed(1); // bottom Y
  const l = (2 + r * 0.5).toFixed(1); // left X
  const ri = (SVG_SIZE - 2 - r * 0.5).toFixed(1); // right X
  const cm = (SVG_SIZE / 2).toFixed(1); // center

  const path = [
    `M${l},${t}`,
    `C${(SVG_SIZE * 0.3).toFixed(1)},${(parseFloat(t) - r * 0.4).toFixed(1)}`,
    `${cm},${(parseFloat(t) + r * 0.3).toFixed(1)}`,
    `${ri},${t}`,
    `C${(parseFloat(ri) + r * 0.3).toFixed(1)},${(SVG_SIZE * 0.3).toFixed(1)}`,
    `${(parseFloat(ri) - r * 0.2).toFixed(1)},${cm}`,
    `${ri},${b}`,
    `C${cm},${(parseFloat(b) + r * 0.4).toFixed(1)}`,
    `${(SVG_SIZE * 0.3).toFixed(1)},${(parseFloat(b) - r * 0.3).toFixed(1)}`,
    `${l},${b}`,
    `C${(parseFloat(l) - r * 0.3).toFixed(1)},${cm}`,
    `${(parseFloat(l) + r * 0.2).toFixed(1)},${(SVG_SIZE * 0.3).toFixed(1)}`,
    `${l},${t}Z`,
  ].join(' ');

  return (
    `<svg xmlns='http://www.w3.org/2000/svg' width='${SVG_SIZE}' height='${SVG_SIZE}'>` +
    `<path fill='none' stroke='${encodedStroke}' stroke-width='${strokeWidth}' ` +
    `stroke-linecap='round' stroke-linejoin='round'${dashAttr} d='${path}'/>` +
    `</svg>`
  );
}

function toDataURI(svg: string): string {
  return `url("data:image/svg+xml,${svg}")`;
}

function generate(): void {
  const lines: string[] = [
    '// Generated by bin/generate-wired-assets.ts — seed: 42',
    '// Regenerate: pnpm tsx bin/generate-wired-assets.ts',
    '//',
    `// SVG size: ${SVG_SIZE}x${SVG_SIZE}  border-image-slice: ${SLICE}  border-image-width: ${BORDER_WIDTH}px`,
    '',
  ];

  for (const state of STATES) {
    const svg = generateSVG(state);
    const uri = toDataURI(svg);
    lines.push(`@wired-svg-${state.name}: ${uri};`);
  }

  writeFileSync(OUTPUT, lines.join('\n') + '\n', 'utf8');
  console.log(`Wrote ${OUTPUT}`);
  console.log(
    `Total size: ${(Buffer.byteLength(lines.join('\n'), 'utf8') / 1024).toFixed(1)} KB`,
  );
}

generate();
